
import java.util.*;

public class $04RelationalOperators {

    public static void main(String args[]) {
        
        Scanner sc = new Scanner(System.in);

        System.out.println("Enter 1st integer:");
        int a = sc.nextInt();
        System.out.println("Enter 2nd integer:");
        int b = sc.nextInt();

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);
    }
}
