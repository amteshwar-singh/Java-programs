// print the largest of 2(1 or3)

public class $02IfElseExample {

    public static void main(String args[]) {
        int a = 1, b = 3;
        if (b > a) {
            System.out.println("A is the largest of 2");

        } else {
            System.out.println("B is the largest of 2");
        }

    }
}
